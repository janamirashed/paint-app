package com.example.paintapp.dtos;

public class ShapeDTO {

    private String type;

    private int x;
    private int y;

    private String fillColor;
    private float borderWidth;
    private float borderColor;

    private Integer x2;
    private Integer y2;

    private Integer radius;

    private Integer width;
    private Integer height;

    private Integer x3;
    private Integer y3;

    private Integer sideLength;

    private Integer radiusX;
    private Integer radiusY;

    public ShapeDTO() {}

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public String getFillColor() {
        return fillColor;
    }

    public void setFillColor(String fillColor) {
        this.fillColor = fillColor;
    }

    public float getBorderWidth() {
        return borderWidth;
    }

    public void setBorderWidth(float borderWidth) {
        this.borderWidth = borderWidth;
    }

    public float getBorderColor() {
        return borderColor;
    }

    public void setBorderColor(float borderColor) {
        this.borderColor = borderColor;
    }

    public Integer getX2() {
        return x2;
    }

    public void setX2(Integer x2) {
        this.x2 = x2;
    }

    public Integer getY2() {
        return y2;
    }

    public void setY2(Integer y2) {
        this.y2 = y2;
    }

    public Integer getRadius() {
        return radius;
    }

    public void setRadius(Integer radius) {
        this.radius = radius;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Integer getX3() {
        return x3;
    }

    public void setX3(Integer x3) {
        this.x3 = x3;
    }

    public Integer getY3() {
        return y3;
    }

    public void setY3(Integer y3) {
        this.y3 = y3;
    }

    public Integer getSideLength() {
        return sideLength;
    }

    public void setSideLength(Integer sideLength) {
        this.sideLength = sideLength;
    }

    public Integer getRadiusX() {
        return radiusX;
    }

    public void setRadiusX(Integer radiusX) {
        this.radiusX = radiusX;
    }

    public Integer getRadiusY() {
        return radiusY;
    }

    public void setRadiusY(Integer radiusY) {
        this.radiusY = radiusY;
    }
}
